enum class AppLanguage(val code: String, val displayName: String, val flag: String) {
    VIETNAMESE("vi", "Tiếng Việt", "🇻🇳"),
    ENGLISH("en", "English", "🇺🇸"),
    CHINESE("zh", "中文", "🇨🇳"),
    SPANISH("es", "Español", "🇪🇸"),
    FRENCH("fr", "Français", "🇫🇷"),
    JAPANESE("ja", "日本語", "🇯🇵"),
    KOREAN("ko", "한국어", "🇰🇷"),
    RUSSIAN("ru", "Русский", "🇷🇺"),
    GERMAN("de", "Deutsch", "🇩🇪"),
    PORTUGUESE("pt", "Português", "🇵🇹"),
    ITALIAN("it", "Italiano", "🇮🇹"),
    TAGALOG("tl", "Tagalog", "🇵🇭"),
    INDONESIAN("id", "Bahasa Indonesia", "🇮🇩"),
    THAI("th", "ไทย", "🇹🇭");

    companion object {
        fun fromCode(code: String): AppLanguage {
            val lowercaseCode = code.lowercase()
            return values().find { 
                lowercaseCode == it.code || lowercaseCode.startsWith(it.code + "-") || lowercaseCode.startsWith(it.code + "_")
            } ?: ENGLISH
        }
    }
}

